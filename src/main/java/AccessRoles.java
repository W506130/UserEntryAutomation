public class AccessRoles {

  private Long accessRoleOid;
  private String description;
  private Long clientGroupMid;
  public Long getAccessRoleOid() {
    return accessRoleOid;
  }

  public void setAccessRoleOid(Long accessRoleOid) {
    this.accessRoleOid = accessRoleOid;
  }

  public String getDescription() {
    return description;
  }

  public void setDescription(String description) {
    this.description = description;
  }

  public Long getClientGroupMid() {
    return clientGroupMid;
  }

  public void setClientGroupMid(Long clientGroupMid) {
    this.clientGroupMid = clientGroupMid;
  }
}
